# Arithmetic Operators
# Used for mathematical operations.


x: int = 16
y: int = 3


# Addition
print("x + y =", x + y)   # 19

# Subtraction
print("x - y =", x - y)    # 13

# Multiplication
print("x * y =", x * y)    # 48

# Division
print("x / y =", x / y)    # 5.33333333

# Floor Division
print("x // y =", x // y)   # 5

# Modulus (remainder)
print("x % y =", x % y)    # 1

# Exponentiation
print("x ** y =", x ** y)   # 4096   ( x * x * x)


