# Comparison( Relational) Operators
# Used to compare values. Returns True or False.

a: int = 12
b: int = 6

# Equal to
print("a == b =", a == b)  # False

# Not equal to
print("a != b =", a != b)  # True

# Greater than
print("a > b =", a > b)   # True

# Less than
print("a < b =", a < b)   # False

# Greater than or equal to
print("a >= b =", a >= b)  # True

# Less than or equal to
print("a <= b =", a <= b)  # False


